operatorA = 3
operatorB = 2

# Resta
minus = operatorA - operatorB
print(f'Resultado resta: {minus}')

# multiply
multiplicate = operatorA * operatorB
print(f'Resultado multiplicación: {multiplicate}')

# Division
div = operatorA / operatorB
print(f'Resultado división: {div}')

# division entera
div = operatorA // operatorB
print(f'Resultado división: {div}')

# modulo o residuo
modulo = operatorA % operatorB
print(f'Resultado residuo división (módulo): {modulo}')

# potencia
pot = operatorA ** operatorB
print(f'Resultado potenica de base {operatorA} exponente {operatorB}: {pot}')