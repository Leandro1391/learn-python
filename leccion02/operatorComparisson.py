a = 4
b = 2

# El lado derecho es lo que se evalua primero
result = (a == b)
print(f'Resultado == : {result}')

result = (a != b)
print(f'Resultado != : {result}')

result = a > b
print(f'Resultado > : {result}')

result = a >= b
print(f'Resultado >= : {result}')

result = a < b
print(f'Resultado < : {result}')

result = a <= b
print(f'Resultado <= : {result}')
